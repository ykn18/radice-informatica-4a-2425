    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        albumName = textAlbumName.getText();
        year = Integer.parseInt(textYear.getText());
        
        switch(selectedType){
            case "CD":
                capacity = Integer.parseInt(textCapacity.getText());
                CD c = new CD(capacity, year, albumName);
                albums.add(c);
                break;
            case "Vinile":
                diameter = Integer.parseInt(textDiameter.getText());
                Vinile v = new Vinile(diameter, year, albumName);   
                albums.add(v);
                break;
        }
        System.out.println(albums);  
        clear();
    }//GEN-LAST:event_insertBtnActionPerformed

    private void clear(){
        textAlbumName.setText(defaultText);
        textYear.setText(defaultText);
        textCapacity.setText(defaultText);
        textDiameter.setText(defaultText);
    }
