    private void radioCDStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_radioCDStateChanged
        if(radioCD.isSelected()) {
            selectedType = "CD";
            textCapacity.setVisible(true);
            labelCapacity.setVisible(true);
            textDiameter.setVisible(false);
            labelDiameter.setVisible(false);
        } else {
            selectedType = "Vinile";
            textCapacity.setVisible(false);
            labelCapacity.setVisible(false);
            textDiameter.setVisible(true);
            labelDiameter.setVisible(true);
        }
    }//GEN-LAST:event_radioCDStateChanged

