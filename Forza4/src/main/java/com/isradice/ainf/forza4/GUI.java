      private void handleMove(int col) {
        if (!game.dropDisc(col)) return;

        updateBoard();

        if (game.isGameOver()) {
            if (game.isDraw()) {
                JOptionPane.showMessageDialog(this, "It's a draw!");
            } else {
                String winner = (game.getWinner() == ConnectFourGame.CellColor.RED) ? "Red" : "Yellow";
                JOptionPane.showMessageDialog(this, winner + " wins!");
            }
            game.reset();
            updateBoard();
        }
    }

    private void updateBoard() {
        for (int r = 0; r < ConnectFourGame.ROWS; r++) {
            for (int c = 0; c < ConnectFourGame.COLS; c++) {
                cellLabels[r][c].setColor(game.getCell(r, c));
            }
        }
    }


